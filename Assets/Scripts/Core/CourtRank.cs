namespace Ginei
{
    /// <summary>
    /// 位階（いかい）＝日本の律令制の官人の身分序列（官僚制基盤・史実参考）。正一位を最上とし少初位下までの
    /// 30 階＋無位。<b>官職（<see cref="Office"/>）とは別軸</b>で、人物の格を表す（官位相当制で官職と対応づく）。
    /// 一位〜三位は正従のみ、四位〜八位は正従×上下、初位は大少×上下。<b>従五位下以上＝貴族（五位の壁）</b>、
    /// <b>従三位以上＝公卿</b>。解決は <see cref="JapaneseCourtRankRules"/>（叙位・官位相当）と
    /// <see cref="OnishikiRules"/>（蔭位＝門閥）が窓口。enum 値の並び順＝高→低（小さいほど上位）。
    /// </summary>
    public enum CourtRank
    {
        正一位, 従一位,
        正二位, 従二位,
        正三位, 従三位,
        正四位上, 正四位下, 従四位上, 従四位下,
        正五位上, 正五位下, 従五位上, 従五位下,
        正六位上, 正六位下, 従六位上, 従六位下,
        正七位上, 正七位下, 従七位上, 従七位下,
        正八位上, 正八位下, 従八位上, 従八位下,
        大初位上, 大初位下, 少初位上, 少初位下,
        無位   // 位階を持たない（出身前・庶民・蔭位の資格なし）。enum 末尾＝最下。
    }
}
