namespace Ginei
{
    /// <summary>
    /// 武器種（#2256）。三すくみ的な編成の読み合いを生む武装カテゴリ。
    /// ビーム＝万能、ミサイル＝万能、長距離砲＝対旗艦特化、対小型＝対配下艦特化、点防御＝汎用控えめ。
    /// </summary>
    public enum WeaponType
    {
        ビーム,     // 通常主砲（万能・中立）
        ミサイル,   // 誘導弾（万能・中立。手動攻撃のミサイルモードとは別管理）
        長距離砲,   // 大口径遠距離砲（対旗艦特化・対配下艦弱）
        対小型,     // 近接速射砲（対配下艦特化・対旗艦弱）
        点防御,     // 防空砲（どちらも控えめ。守勢的な武装）
    }
}
